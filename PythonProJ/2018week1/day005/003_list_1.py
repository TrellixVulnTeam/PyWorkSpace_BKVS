a =['万达','珠儿','张三','李四','龙五','赵六', '孙七', '周八', '吴九', '郑十']



#显示数组数据
print(a[2])


#范围取值(切片)

print(a[1:3])

#取最后一个元素
print(a[1:-1])

#不写的时候,显示全部
print(a[1:])

#步长式取值
print(a[1::2])

#反向步长取值
print(a[3::-2])

#全部反向从右往左
print(a[4::-2])

#右边第二个元素开始,每隔一个循环一次
print(a[-2::-1])